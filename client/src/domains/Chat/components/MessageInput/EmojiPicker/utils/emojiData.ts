export function getEmojisByCategory(category: string): EmojiItem[] {
  return EMOJI_DATA.filter(emoji => emoji.category === category);
}

export function searchEmojis(query: string): EmojiItem[] {
  if (!query.trim()) return [];
  
  const lowerQuery = query.toLowerCase();
  return EMOJI_DATA.filter(emoji => 
    emoji.name.toLowerCase().includes(lowerQuery) ||
    emoji.keywords.some(kw => kw.toLowerCase().includes(lowerQuery))
  ).slice(0, 50);
}
